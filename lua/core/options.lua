-- Shorthand
local opt = vim.opt

-- Line Number
opt.number = true
opt.relativenumber = false

-- Line Wrap
opt.wrap = false

-- Tab
opt.tabstop = 2
opt.shiftwidth = 2
opt.softtabstop = 2
opt.expandtab = true

-- Auto Indent
opt.autoindent = true

-- Search
opt.ignorecase = true
opt.smartcase = true

-- Cursor Line
opt.cursorline = true

-- Backup
opt.backup = false
opt.writebackup = false

-- Update Time (Default 4000ms)
opt.updatetime = 300

-- True Colors
opt.termguicolors = true

-- Swapfile
opt.swapfile = false

-- Scroll Off
opt.scrolloff = 10

-- Sign Column
opt.signcolumn = "yes"

-- Clipboard
opt.clipboard = "unnamedplus"

